package Day17;
//Java Bean Example
public class JavaBean {

	
		private String ad;
	    private int yas;  //private

	    public JavaBean() {
	    }

	    public String getAd() {
	        return ad;
	    }

	    public void setAd(String ad) {
	        this.ad = ad;
	    }

	    public int getYas() {
	        return yas;
	    }

	    public void setYas(int yas) {
	        this.yas = yas;
	    }
}
